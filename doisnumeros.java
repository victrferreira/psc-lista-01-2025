package doisnumeros;

public class doisnumeros {
    public static void main(String[] args) {
        int number1 = 4;
        int number2 = 2;
        int subtração = number1 - number2;
        int multiplicação = number1 * number2;
        int divisão = number1 / number2;
        System.out.println("number1 = " + number1);
        System.out.println("number2 = " + number2);
        System.out.println("Valor de number1 - number2 = " + subtração);
        System.out.println("number1 x number2= " + multiplicação);
        System.out.println("number1 dividido por number2= " + divisão);
    }
}
